public class loop {
    public static void main(String[] args) {
        System.out.println("Using While Loop:");
        int i = 1;
        while (i <= 10) {
            System.out.println("Manju");
            i++;
        }
        System.out.println("\nUsing For Loop:");
        for (int j = 1; j <= 7; j++) {
            System.out.println("java coding ");
        }
    }
}   